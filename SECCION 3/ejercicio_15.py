# Expresión original
resultado = 4 * 2 ** 3 / 8 + 1

# Explicación paso a paso
paso1 = 2 ** 3
paso2 = 4 * paso1
paso3 = paso2 / 8
paso4 = paso3 + 1

print("Paso 1: 2 ** 3 =", paso1)
print("Paso 2: 4 * 8 =", paso2)
print("Paso 3: 32 / 8 =", paso3)
print("Paso 4: 4.0 + 1 =", paso4)
print("Resultado final:", resultado)
