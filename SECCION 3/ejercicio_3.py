def resolver_ejercicio():
    print("Ejercicio 3:")
    print("Expresión: (7 + 3) * 2 - 5\n")

    # Paso 1: Resolver el paréntesis
    paso1 = 7 + 3
    print(f"Paso 1: (7 + 3) = {paso1}")

    # Paso 2: Multiplicar el resultado por 2
    paso2 = paso1 * 2
    print(f"Paso 2: {paso1} * 2 = {paso2}")

    # Paso 3: Restar 5
    resultado = paso2 - 5
    print(f"Paso 3: {paso2} - 5 = {resultado}")

    print(f"\n✅ Resultado final: {resultado}")


resolver_ejercicio()
