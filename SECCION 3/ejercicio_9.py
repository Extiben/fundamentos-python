def resolver_ejercicio():
    print("Ejercicio 9:")
    print("Expresión: 10 % 3 + 2 * 5\n")

    # Paso 1: Módulo
    modulo = 10 % 3
    print(f"Paso 1: 10 % 3 = {modulo}")

    # Paso 2: Multiplicación
    multiplicacion = 2 * 5
    print(f"Paso 2: 2 * 5 = {multiplicacion}")

    # Paso 3: Suma
    resultado = modulo + multiplicacion
    print(f"Paso 3: {modulo} + {multiplicacion} = {resultado}")

    print(f"\n✅ Resultado final: {resultado}")


resolver_ejercicio()
