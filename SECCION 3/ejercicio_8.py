def resolver_ejercicio():
    print("Ejercicio 8:")
    print("Expresión: 6 + 4 / 2 ** 2\n")

    # Paso 1: Potencia
    potencia = 2 ** 2
    print(f"Paso 1: 2 ** 2 = {potencia}")

    # Paso 2: División
    division = 4 / potencia
    print(f"Paso 2: 4 / {potencia} = {division}")

    # Paso 3: Suma
    resultado = 6 + division
    print(f"Paso 3: 6 + {division} = {resultado}")

    print(f"\n✅ Resultado final: {resultado}")


resolver_ejercicio()
