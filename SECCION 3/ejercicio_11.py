# Expresión original
resultado = 7 + 2 * (3 + 5) / 4

# Explicación paso a paso
paso1 = 3 + 5
paso2 = 2 * paso1
paso3 = paso2 / 4
paso4 = 7 + paso3

print("Paso 1: (3 + 5) =", paso1)
print("Paso 2: 2 * 8 =", paso2)
print("Paso 3: 16 / 4 =", paso3)
print("Paso 4: 7 + 4.0 =", paso4)
print("Resultado final:", resultado)
